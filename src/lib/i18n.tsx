export type Language = keyof typeof translations
export type TranslationKey = string

interface LanguageContextType {
  language: Language
  setLanguage: (lang: Language) => void
  t: (key: string) => string
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined)

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>("fr")

  useEffect(() => {
    const savedLanguage = localStorage.getItem("language") as Language
    if (savedLanguage && (savedLanguage === "fr" || savedLanguage === "en")) {
      setLanguage(savedLanguage)
    }
  }, [])

  const changeLanguage = (lang: Language) => {
    setLanguage(lang)
    localStorage.setItem("language", lang)
  }

  const t = (key: string): string => {
    const keys = key.split(".")
    let value: any = translations[language]

    for (const k of keys) {
      value = value?.[k]
    }

    return value || key
  }

  return (
    <LanguageContext.Provider value={{ language, setLanguage: changeLanguage, t }}>{children}</LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider")
  }
  return context
}

// Updated hook to react to context changes.
export function useTranslation(lang?: Language) {
  // Prefer current language from context when available and no explicit lang override.
  const context = useContext(LanguageContext)

  if (lang === undefined && context) {
    // Simply reuse the context implementation – stays in sync with LanguageProvider.
    return { t: context.t }
  }

  const targetLang: Language = lang ?? "fr"

  return {
    t: (key: string) => {
      const keys = key.split(".")
      let value: any = translations[targetLang]

      for (const k of keys) {
        value = value?.[k]
      }

      return value || key
    },
  }
}
